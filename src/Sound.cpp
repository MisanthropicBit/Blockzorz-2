#include "Sound.h"

//=========================================================================================================================

Sound::Sound(const string& file)
{
	sound = NULL;
	if(!file.empty()) {sound = Mix_LoadWAV(file.c_str());}
	
	if(!sound)
	{
		Mix_FreeChunk(sound);
		sound = NULL;
	}
}

//============================================================================================================================

Sound::~Sound()
{
	if(sound) {Mix_FreeChunk(sound);}
	sound = NULL;
}

//============================================================================================================================

Mix_Chunk* Sound::GetSound() const
{
	if(sound)
	{
		return sound;
	}
	else
	{
		return NULL;
	}
}

//============================================================================================================================

bool Sound::IsValid()
{
	return (sound != NULL);
}

